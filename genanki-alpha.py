import genanki

# ==============================================================================
# Unique model id 
# ==============================================================================
model_id = 1948390957

# ==============================================================================
# How to create a model
# ==============================================================================
my_model = genanki.Model(
  model_id,
  'Simple Model',
  fields=[
    {'name': 'Question'},
    {'name': 'Answer'},
  ],
  templates=[
    {
      'name': 'Card 1',
      'qfmt': '{{Question}}',
      'afmt': '{{FrontSide}}<hr id="answer">{{Answer}}',
    },
  ])

# ==============================================================================
# How to created a Note
# ==============================================================================
my_note = genanki.Note(
    model=my_model,
    fields=["Capital de Pa√±aes", "Ridmad"]
)


# ==============================================================================
# How to create a Deck
# ==============================================================================
my_deck = genanki.Deck(
  1152671212,
  'Country Capitals')

# ------------------------------------------------------------------------------
# Add notes to your deck
# ------------------------------------------------------------------------------
my_deck.add_note(my_note)


# ------------------------------------------------------------------------------
# Save the deck locally
# ------------------------------------------------------------------------------
genanki.Package(my_deck).write_to_file('output.apkg')